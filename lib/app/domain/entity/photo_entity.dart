import 'package:wallpaper_app/app/domain/entity/src_entity.dart';

class PhotoEntity {
  int? id;
  int? width;
  int? height;
  String? url;
  String? photographer;
  String? photographerUrl;
  int? photographerId;
  String? avgColor;
  SrcEntity? src;
  bool? liked;
  String? alt;

  PhotoEntity(
      {this.id,
      this.width,
      this.height,
      this.url,
      this.photographer,
      this.photographerUrl,
      this.photographerId,
      this.avgColor,
      this.src,
      this.liked,
      this.alt});
  @override
  String toString() {
    return 'PhotoEntity{id: $id, width: $width, height: $height, url: $url, photographer: $photographer, photographerUrl: $photographerUrl, photographerId: $photographerId, avgColor: $avgColor, src: $src, liked: $liked, alt: $alt}';
  }
}
